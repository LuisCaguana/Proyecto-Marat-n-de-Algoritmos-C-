#include <iostream>
using namespace std;

int main() {
    int a, b, producto;

    cout << "Ingrese el primer número: ";
    cin >> a;

    cout << "Ingrese el segundo número: ";
    cin >> b;

    producto = a * b;

    cout << "La multiplicación de " << a << " x " << b << " es: " << producto << endl;

    return 0;
}
